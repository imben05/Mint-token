  const { getNamedAccounts } = hre;
  const { deployer } = await getNamedAccounts();

  const [signer] = await ethers.getSigners();

  console.log("â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•");
  console.log("ğŸš€ DEPLOYING MyMintToken & MerkleAirdrop (UUPS Proxy)");
  console.log("â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•");
  console.log("Deployer:", deployer);
  console.log("Signer:", signer.address);
  console.log("");

  // â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
  // 1ï¸âƒ£ Deploy MyMintToken (non-upgradeable)
  // â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
  const TokenFactory = await ethers.getContractFactory("MyMintToken");
  const token = await TokenFactory.deploy("MyMintToken", "MMT", deployer);
  await token.waitForDeployment();

  const tokenAddress = await token.getAddress();
  console.log("âœ… MyMintToken deployed at:", tokenAddress);

  // â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
  // 2ï¸âƒ£ Deploy MerkleAirdropUpgradeable (UUPS Proxy)
  // â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
  const AirdropFactory = await ethers.getContractFactory("MerkleAirdropUpgradeable");

