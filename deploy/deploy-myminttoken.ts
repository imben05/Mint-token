  const airdropAddress = await proxy.getAddress();

  console.log("âœ… MerkleAirdrop Proxy deployed at:", airdropAddress);

  // â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
